
public class RelationalOparators {
	public static void main(String args[])
	{
		int a=5,b=10;
		System.out.println("a is "+a+" b is "+b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
	}

}
